package Solve_2a;

public class Bus {
    private int speed;
    private int no_of_passenger;

    public int getSpeed(){
        return speed;
    }

    public int getPassenger(){
        return no_of_passenger;
    }

    public void setSpeed(int speed){
        this.speed = speed;
    }
    
    public void setPassenger(int no_of_passenger){
        this.no_of_passenger = no_of_passenger;
    }
}
